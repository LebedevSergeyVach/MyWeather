/**
 * Перечисление возможных позиций иконки в кнопке относительно текста.
 *
 * @property [Start] Иконка располагается перед текстом (слева для `LTR`, справа для `RTL`)
 * @property [End] Иконка располагается после текста (справа для `LTR`, слева для `RTL`)
 */
enum class IconPosition {
    Start, End
}
